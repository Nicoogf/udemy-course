public class SentenciaSwich {
    public static void main(String[] args) {
        int valor_uno = 10 ;

        switch (valor_uno){
            case 5 :
                System.out.println("El valor ingresado fue 5");
                break;
            case 10 :
                System.out.println("El sistema retorno 10");
                break;
            default:
                System.out.println("No hago nada");

        }
    }
}
